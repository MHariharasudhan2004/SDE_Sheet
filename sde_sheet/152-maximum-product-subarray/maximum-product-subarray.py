class Solution:
    def maxProduct(self, nums: List[int]) -> int:
        curr_max=curr_min=1
        res=max(nums)
        for n in nums:
            temp=curr_max*n
            curr_max=max(temp,curr_min*n,n)
            curr_min=min(temp,curr_min*n,n)
            res=max(res,curr_max)
        return res
